    p.mb-4(data-aos="fade-left") Las desventajas del método de Leopold es la falta de objetividad, ya que no se puede distinguir de los impactos generados a corto plazo y los de largo plazo y los valores de calificación varían de acuerdo con la percepción del calificador entre (1 - 10) y (1 - 5); además, parte del hecho de que los eventos tienen un porcentaje de probabilidad de ocurrencia del 100 %.

    p.mb-4(data-aos="fade-left") #[b Listas de chequeo:] son herramientas fáciles de usar que permiten la identificación de cada impacto de acuerdo a cada actividad propuesta. Existen dos tipos de listas, la primera, de listados simples, que contiene cada uno de los factores ambientales (suelo, agua, flora, aire, etc.), esto garantiza que ningún factor quede por fuera de la evaluación; la segunda, que contiene cuestionarios y consiste en una serie de preguntas generales y con dos o más respuestas que al momento de calificar, nos entregan la evaluación de los impactos generados por la industria. 

    p.mb-4(data-aos="fade-left") La desventaja es que se limita en la apreciación y no identifican el tipo de riesgo, la probabilidad de ocurrencia, los impactos indirectos, ni la importancia de cada uno. 

    p.mb-4(data-aos="fade-left") #[b Diagramas de flujo:] aquí se identifican impactos a partir de establecer relaciones causales entre componentes, no son exhaustivos, ni puntuales, sino que plantean puntos críticos (Reinoso, 2014). Este método permite la evaluación de los impactos indirectos generados ya que tiene en cuenta la relación causa y efecto. 

